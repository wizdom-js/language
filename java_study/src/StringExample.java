public class StringExample {
    public static void main(String[] args) {
        System.out.println("안녕하세요 저는 누구누"); // 문자열
        System.out.println('나'); // 문자
        System.out.println("\"자바\"를 공부해요");
        System.out.println("자바는 쉬워요. \n자바는 어려워요.");
        // 이건 주석이에오
        /* 이건
        여러줄의
        주석입니다.
         */
    }
}
